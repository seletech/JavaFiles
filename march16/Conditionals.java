package javaclass;

public class Conditionals {

	public static void main(String[] args) {
	 
		
		// variables, data types, condtional if else for 
		String str = "bb";
		switch(str) {
		
		case "a": System.out.println("it's value is a"); break;
		case "bb": System.out.println("it's value is bb"); break;
		case "cc": System.out.println("it's value is cc"); break;
		case "bbb": System.out.println("it's value is bbb");break;
		default:  System.out.println("Not in given values");
		}
		
	}

}
